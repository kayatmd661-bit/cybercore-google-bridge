            'ড়', 'ঢ়', 'য়', 'ৎ', 'ং', 'ঃ', 'ঁ' -> '9'
            '্', '0', ' ' -> '0'
            else -> '1'
        }
    }

    // Convert word to base alphabet key sequence (stripping implicit Kars and Hasant)
    fun wordToBaseSequence(word: String): String {
        val sb = StringBuilder()
        for (ch in word) {
            if (isKar(ch) || ch == '্') continue // Strip Kars and Hasanta for base sequence mapping
            sb.append(getT9KeyForChar(ch))
        }
        return sb.toString()
    }

    // Alias for wordToSequence
    fun wordToSequence(word: String): String = wordToBaseSequence(word)

    // Convert word to full key sequence (including 1 for Kars)
    fun wordToFullSequence(word: String): String {
        val sb = StringBuilder()
        for (ch in word) {
            sb.append(getT9KeyForChar(ch))
        }
        return sb.toString()
    }

    fun isKar(ch: Char): Boolean {
        return ch in listOf('া', 'ি', 'ী', 'ু', 'ূ', 'ৃ', 'ে', 'ৈ', 'ো', 'ৌ')
    }

    fun isVowel(ch: Char): Boolean {
        return ch in listOf('অ', 'আ', 'ই', 'ঈ', 'উ', 'ঊ', 'ঋ', 'এ', 'ঐ', 'ও', 'ঔ')
    }

    fun isConsonant(ch: Char): Boolean {
        return ch in 'ক'..'হ' || ch in 'ড়'..'য়'
    }

